numero1 = input('Digite um numero:')
numero1 = int(numero1)

numero2 = input('Digite outro numero:')
numero2 = int(numero2)

if numero1 == numero2: #operador de igualdade
    print('o numero %d é igual a %d.'%(numero1,numero2))
if numero1 != numero2:
    print('o numero %d é diferente de %d.'%(numero1,numero2))
if numero1 < numero2:
    print('o numero %d é menor que %d.'%(numero1,numero2))
if numero1 > numero2:
    print('o numero %d é maior que  %d.'%(numero1,numero2))
if numero1 >= numero2:
    print('o numero %d é maior ou igual a %d.'%(numero1,numero2))
if numero1 <= numero2:
    print('o numero %d é menor ou igual a %d.'%(numero1,numero2))
